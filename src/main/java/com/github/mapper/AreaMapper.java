package com.github.mapper;

import org.apache.ibatis.annotations.Insert;
import org.apache.ibatis.annotations.Mapper;
import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Map;


@Mapper
@Repository
public interface AreaMapper {

	@Insert("INSERT INTO t_area(code, name) VALUES(#{code}, #{name})")
	void add(@Param("code") Integer code, @Param("name") String name);

	@Select("SELECT * FROM t_area WHERE code = #{code}")
	Map<Integer, String> get(Integer code);

	@Select("SELECT * FROM t_area WHERE name LIKE concat('%', #{code}, '%')")
	List<Map<Integer, String>> search(String name);
}
